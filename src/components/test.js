import { ethers } from "ethers";
const provider = new ethers.JsonRpcProvider(
  "https://optimism-mainnet.infura.io/v3/b811b8acd4af4785b0f574ebe8df1685"
); // Use Ethereum mainnet

const aggregatorV3InterfaceABI = [
  {
    inputs: [],
    name: "decimals",
    outputs: [{ internalType: "uint8", name: "", type: "uint8" }],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "description",
    outputs: [{ internalType: "string", name: "", type: "string" }],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [{ internalType: "uint80", name: "_roundId", type: "uint80" }],
    name: "getRoundData",
    outputs: [
      { internalType: "uint80", name: "roundId", type: "uint80" },
      { internalType: "int256", name: "answer", type: "int256" },
      { internalType: "uint256", name: "startedAt", type: "uint256" },
      { internalType: "uint256", name: "updatedAt", type: "uint256" },
      { internalType: "uint80", name: "answeredInRound", type: "uint80" },
    ],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "latestRoundData",
    outputs: [
      { internalType: "uint80", name: "roundId", type: "uint80" },
      { internalType: "int256", name: "answer", type: "int256" },
      { internalType: "uint256", name: "startedAt", type: "uint256" },
      { internalType: "uint256", name: "updatedAt", type: "uint256" },
      { internalType: "uint80", name: "answeredInRound", type: "uint80" },
    ],
    stateMutability: "view",
    type: "function",
  },
  {
    inputs: [],
    name: "version",
    outputs: [{ internalType: "uint256", name: "", type: "uint256" }],
    stateMutability: "view",
    type: "function",
  },
];

const addr = "0xD702DD976Fb76Fffc2D3963D037dfDae5b04E593"; // Mainnet BTC/USD feed
const priceFeed = new ethers.Contract(addr, aggregatorV3InterfaceABI, provider);
// console.log(priceFeed);

priceFeed.latestRoundData().then((roundData) => {
  const price = roundData.answer.toString();
  const decimals = 8; // BTC/USD has 8 decimals
  const formattedPrice = price / 10 ** decimals;
  console.log(`: $${formattedPrice}`);
});
